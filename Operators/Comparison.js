// Comparison Operators

console.log("Comparison Operators");
console.log("==:", a == b);
console.log("===:", "hello" === 10); //strictly equal to checks both val and data-type
console.log("!=", a != b);
console.log(">", a > b);
console.log("<", a < b);
console.log("<=", a <= b);
console.log(">=", a >= b);
